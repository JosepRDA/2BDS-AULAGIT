#Numero inteiro - INT - Representa um numero inteiro, positivo e negativo, sem casa decimal
numeroInteiro: int = 1000

#NUMERO DECIMAL - FLOAT - representa números com casas decimais
numeroDecimal: float = 999.99

# TEXTO SIMPLES OU COMPOSTO - STRING
# Representa cadeias de caracteres, ou seja, texto.
textoString: str = "Bem vindo a linguagem python"

# VALOR LÓGICO - BOOLEAN
# representa valores lógicos, podendo ser true ou false
valorLogico = True

print("variáveis: ")
print(numeroInteiro)
print(numeroDecimal)
print(textoString)
print(valorLogico)

print("\ntipos: ")
print(type(numeroInteiro))
print(type(numeroDecimal))
print(type(textoString))
print(type(valorLogico))

#LISTA - LIST - Coleções ordenadas e mutáveis de elementos
lista = [34,65,34,8,6,345,75]

#TUPLAS - TUPLE - Semelhante às listas, mas são imutáveis
tupla = (43,65)
